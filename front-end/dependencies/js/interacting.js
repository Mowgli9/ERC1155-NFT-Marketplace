export async function isUserLogged() {
  accounts = await ethereum.request({ method: "eth_accounts" })
  if (accounts.length === 0) {
    wallet_btn.textContent = "Connect MetaMask"
  }
  else {
    wallet_btn.textContent = "Connected"
  }
}

// if user swap accounts we need to make some changes

window.ethereum.on("accountsChanged", async ()=>{
  if (typeof window.ethereum !== "undefined") {
    try {
      await ethereum.request({ method: "eth_accounts" })
    } catch (error) {
    }
    wallet_btn.textContent = "Connected"
    accounts = await ethereum.request({ method: "eth_accounts" })
    location.reload()
  } else {
    alert('we cant find metamask installed')
  }
})



async function connect() {
  if (typeof window.ethereum !== "undefined") {
    try {
      await ethereum.request({ method: "eth_requestAccounts" })
      location.reload()
    } catch (error) {
    }

  } else {
    alert('we cant find metamask installed')
  }
}
// connect wallet button 
wallet_btn.addEventListener("click",async ()=>{
  connect()
})

